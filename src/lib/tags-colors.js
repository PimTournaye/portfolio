/**
 * @description Get the corresponding color for a given tag.
 * @param {string} tag
 * @returns {string} color
 */
export default function tagColor(tag) {
  switch (tag) {
    case 'Creative Coding':
      return '#FFFFFF';
    case 'Maker':
      return '#FFFFFF';
    case 'Music':
      return '#FFFFFF';
    case 'Machine Learning & AI':
      return '#FFFFFF';
    case 'Generative':
      return '#FFFFFF';
    case 'Web Dev':
      return '#FFFFFF';
    case 'Physical Computing':
      return '#FFFFFF';
    case 'Experience':
      return '#FFFFFF';
    case 'UX/UI':
      return '#FFFFFF';
    case 'Other':
      return '#FFFFFF';
    default:
      return '#FFFFFF';
  }
}
