/**
 * @description Get the corresponding color for a given tag.
 * @param {string} tag
 * @returns {string} color
 */
export default function tagColor(tag) {
	let color;

	switch (tag) {
		case 'Creative Coding':
			color = '#FFFFFF';
			break;

		case 'Maker':
			color = '#FFFFFF';
			break;

		case 'Music':
			color = '#FFFFFF';
			break;

		case 'Machine Learning & AI':
			color = '#FFFFFF';
			break;

		case 'Generative':
			color = '#FFFFFF';
			break;

		case 'Web Dev':
			color = '#FFFFFF';
			break;

		case 'Physical Computing':
			color = '#FFFFFF';
			break;

		case 'Experience':
			color = '#FFFFFF';
			break;

		case 'UX/UI':
			color = '#FFFFFF';
			break;

		case 'Other':
			color = '#FFFFFF';
			break;

		default:
			break;
	}

	return color;
}
